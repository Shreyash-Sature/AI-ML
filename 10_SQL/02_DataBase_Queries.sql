-- DATABASE Queries

-- 1. CREATE DATABASE (USED TO CREATE NEW DATABASE)
CREATE DATABASE db_name;
CREATE DATABASE IF NOT EXISTS db_name;

-- 2. USE (specifies which database to use)
USE db_name;

-- 3. SHOW DATABASE (shows all databases)
SHOW DATABASES;

-- 4. SHOW TABLES (shows tables in database)
SHOW TABLES;

-- 4. DROP DATABASE (used to delete database)
DROP DATABASE db_name;